package net.artificialworlds.tunnelwar;

class EnabledPlayers
{
    final int redHumans;
    final int redAis;
    final int greenHumans;
    final int greenAis;

    EnabledPlayers(int redHumans, int redAis, int greenHumans, int greenAis)
    {
        this.redHumans = redHumans;
        this.redAis = redAis;
        this.greenHumans = greenHumans;
        this.greenAis = greenAis;
    }
}
